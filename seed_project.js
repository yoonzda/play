const { PrismaClient } = require('@prisma/client');
const prisma = new PrismaClient();

async function seed() {
  console.log('실제적인 장문의 프로젝트 더미 데이터를 생성합니다...');

  const longContent = `[프로젝트 회고] 새로운 디지털 책장 UI 고도화 작업 완료

이번 프로젝트의 가장 큰 핵심은 "어떻게 하면 사용자가 빈 공간을 압도적인 갤러리처럼 느끼게 할 것인가" 였습니다. 초기의 어색했던 비대칭 구조를 전면 폐기하고, 화면 정중앙에 모든 텍스트가 정렬되어 시선을 모으는 가장 완벽한 에디토리얼 레이아웃을 성공적으로 도입했습니다. 

첫째, 기존에 렌더링되던 원고지 스타일의 다중 줄바꿈(엔터) 파편들을 어떻게 처리할지가 관건이었습니다. 데이터베이스를 건드리지 않으면서 오직 뷰어 자체에서만 연속된 공백 라인을 걷어내는 로직을 도입함으로써, 글이 타이트하고 오밀조밀하게 뭉쳐 가독성이 완전히 극대화되는 마술을 부릴 수 있었습니다. 

둘째, 책기둥에서 흘러나오는 가름끈(Bookmark Ribbon) 디테일입니다. 아무리 플랫하고 미니멀한 UI라도, 어딘가 현실 세계의 종이책이 지닌 물성을 암시하는 메타포가 한두 개쯤 존재해야 유저의 눈이 즐겁기 마련입니다. 상단에서 절묘하게 텍스트 위로 내려앉은 붉은 가름끈은 화이트 톤의 갤러리 화면에 아주 강력한 포인트 컬러를 잡아줍니다.

셋째, 하단의 수정/삭제 액션 버튼을 새롭게 설계했습니다. 무미건조한 텍스트 링크에서 벗어나, 완전히 고급스럽고 매끄러운 알약(Pill) 형태의 토글 스타일로 재작업했습니다. 누를 때마다 부드럽게 섀도우가 생성되며 입체감이 살아나는 인터랙션은 전체적인 프로덕트의 밀도를 한 층 높여줍니다.

앞으로는 이 템플릿을 기반으로 실제 다양한 카테고리의 텍스트들이 아름답게 축적되길 기대합니다. 스크롤을 내리느라 수고하셨습니다. 이것은 충분한 높이 확보를 위해 작성된 긴 문장들이 성공적으로 렌더링되고 있음을 입증하는 좋은 지표가 될 것입니다!`;

  await prisma.post.create({
    data: {
      title: '디지털 아카이브 뷰어 고도화 프로젝트',
      content: longContent,
      category: '프로젝트',
    }
  });
  
  console.log('✅ 프로젝트 더미 추가 완료!');
}

seed()
  .catch(e => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
